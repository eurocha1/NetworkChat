    public class ServerStart implements Runnable 
    {
        @Override
        public void run() 
        {
            clientOutputStreams = new ArrayList();
            users = new ArrayList();  

            try 
            {
                ServerSocket serverSock = new ServerSocket(0);
                ta_chat.append("Port no:  "+serverSock.getLocalPort()+"\n");

                while (true) 
                {
				Socket clientSock = serverSock.accept();
				PrintWriter writer = new PrintWriter(clientSock.getOutputStream());
				clientOutputStreams.add(writer);

				Thread listener = new Thread(new ClientHandler(clientSock, writer));
				listener.start();
				ta_chat.append("Got a connection. \n");
                }
            }
            catch (Exception ex)
            {
                ta_chat.append("Error making a connection. \n");
            }
        }
    }
    
    public void userAdd (String data) 
    {
        String message, add = ": :Connect", done = "Server: :Done", name = data;
        //ta_chat.append("Before " + name + " added. \n");
        users.add(name);
        //ta_chat.append("After " + name + " added. \n");
        String[] tempList = new String[(users.size())];
        users.toArray(tempList);

        for (String token:tempList) 
        {
            message = (token + add);
            tellEveryone(message);
        }
        tellEveryone(done);
    }
    
    public void userRemove (String data) 
    {
        String message, add = ": :Connect", done = "Server: :Done", name = data;
        users.remove(name);
        String[] tempList = new String[(users.size())];
        users.toArray(tempList);

        for (String token:tempList) 
        {
            message = (token + add);
            tellEveryone(message);
        }
        tellEveryone(done);
    }
    
    public void tellEveryone(String message) 
    {
	Iterator it = clientOutputStreams.iterator();
        StringBuffer allClients = new StringBuffer();
        for (int i=0; i<users.size(); i++)
        {
            allClients.append(users.get(i)).append(',');
        } 
        //System.out.println("sz "+users.size()+", b4 while "+allClients);
        while (it.hasNext()) 
        {
            try 
            {
                PrintWriter writer = (PrintWriter) it.next();
                writer.println(message+":"+allClients);
		//ta_chat.append("Sending: " + message + "\n");
                writer.flush();
                ta_chat.setCaretPosition(ta_chat.getDocument().getLength());

            } 
            catch (Exception ex) 
            {
		ta_chat.append("Error telling everyone. \n");
            }
        } 
    }
    
    public void tellFriend(String data[])
    {
        String text = data[1];
        String names = text.substring(0, text.indexOf(" "));
       
        int count = names.length() - names.replace("@", "").length();
        String name[] = names.split("@");
        
        text = text.substring(text.indexOf(" ")+1);
        
        String message = "";
        
        ArrayList<PrintWriter> pw = new ArrayList<PrintWriter>();
        for(int i=1; i<=count; i++)
        {
            pw.add(hm.get(name[i]));
        }
        //PrintWriter pw = hm.get(name);
        
        for(int i=0; i<count; i++)
        {
            if(pw.get(i) == null)
            {
                PrintWriter p = hm.get(data[0]);
                message = data[0] + ":" + name[i+1] + ":" + "Error";
                p.println(message);
                p.flush();
            }
            else
            {
                message = data[0] + ":" + text + ":" + "Chat";
            
                pw.get(i).println(message);
                pw.get(i).flush();
            }
        }
        message = data[0] + ":" + text + ":" + "Chat";
        PrintWriter p = hm.get(data[0]);
        p.println(message);
        p.flush();
    }
    
    public void AlreadyExist(String name)
    {
        PrintWriter p = hm.get(name);
        String message = name + ":" + " " + ":" + "Exist";
        p.println(message);
        p.flush();
    }
    
    public int doesNameExist(String name)
    {
        if(users.contains(name))
        {
            return 1;
        }
        else
        {
            return 0;
        }
    }
    
